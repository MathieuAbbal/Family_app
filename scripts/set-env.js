/**
 * Generates environment files and firebase-messaging-sw.js from .env variables.
 * Run: node scripts/set-env.js
 *
 * Reads from .env file (local dev) or process.env (CI/CD).
 */

const fs = require('fs');
const path = require('path');

// ---- Load .env if present ----
const envPath = path.resolve(__dirname, '..', '.env');
const env = {};

if (fs.existsSync(envPath)) {
  const lines = fs.readFileSync(envPath, 'utf-8').split('\n');
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const idx = trimmed.indexOf('=');
    if (idx === -1) continue;
    const key = trimmed.substring(0, idx).trim();
    const value = trimmed.substring(idx + 1).trim();
    env[key] = value;
  }
}

function get(key) {
  return process.env[key] || env[key] || '';
}

// ---- Validate required vars ----
const required = [
  'FIREBASE_API_KEY', 'FIREBASE_AUTH_DOMAIN', 'FIREBASE_DATABASE_URL',
  'FIREBASE_PROJECT_ID', 'FIREBASE_STORAGE_BUCKET', 'FIREBASE_MESSAGING_SENDER_ID',
  'FIREBASE_APP_ID', 'VAPID_KEY', 'GOOGLE_CLIENT_ID',
  'FAMILY_CALENDAR_ID', 'FAMILY_DRIVE_FOLDER_ID', 'GEOAPIFY_API_KEY'
];

const missing = required.filter(k => !get(k));
if (missing.length) {
  console.error('Missing environment variables:', missing.join(', '));
  console.error('Create a .env file from .env.example or set them in your environment.');
  process.exit(1);
}

