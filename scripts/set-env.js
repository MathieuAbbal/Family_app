// ---- Generate firebase-messaging-sw.js ----
const serviceWorker = `/* eslint-disable no-undef */
// AUTO-GENERATED by scripts/set-env.js — do not edit manually
importScripts('https://www.gstatic.com/firebasejs/10.14.1/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.14.1/firebase-messaging-compat.js');

firebase.initializeApp({
  apiKey: '${get('FIREBASE_API_KEY')}',
  authDomain: '${get('FIREBASE_AUTH_DOMAIN')}',
  projectId: '${get('FIREBASE_PROJECT_ID')}',
  storageBucket: '${get('FIREBASE_STORAGE_BUCKET')}',
  messagingSenderId: '${get('FIREBASE_MESSAGING_SENDER_ID')}',
  appId: '${get('FIREBASE_APP_ID')}'
});

const messaging = firebase.messaging();

messaging.onBackgroundMessage((payload) => {
  const data = payload.data || {};
  const title = data.title || 'FamilyApp';
  const options = {
    body: data.body || 'Nouveau message',
    icon: '/assets/icons/icon-192x192.png',
    badge: '/assets/icons/icon-72x72.png',
    data: { url: data.url || '/' }
  };
  self.registration.showNotification(title, options);
  if (navigator.setAppBadge) {
    navigator.setAppBadge();
  }
});

self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  if (navigator.clearAppBadge) {
    navigator.clearAppBadge();
  }
  const url = event.notification.data?.url || '/';
  event.waitUntil(
    clients.matchAll({ type: 'window', includeUncontrolled: true }).then((windowClients) => {
      for (const client of windowClients) {
        if (client.url.includes(self.location.origin) && 'focus' in client) {
          client.navigate(url);
          return client.focus();
        }
      }
      return clients.openWindow(url);
    })
  );
});
`;

// ---- Write files ----
const envDir = path.resolve(__dirname, '..', 'src', 'environments');
if (!fs.existsSync(envDir)) fs.mkdirSync(envDir, { recursive: true });

fs.writeFileSync(path.join(envDir, 'environment.ts'), environmentDev);
fs.writeFileSync(path.join(envDir, 'environment.prod.ts'), environmentProd);
fs.writeFileSync(path.resolve(__dirname, '..', 'src', 'firebase-messaging-sw.js'), serviceWorker);

console.log('Environment files generated successfully.');
